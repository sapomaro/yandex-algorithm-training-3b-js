/*

7. SNTP

Ограничение времени	1 секунда
Ограничение памяти	64Mb
Ввод	стандартный ввод или input.txt
Вывод	стандартный вывод или output.txt

Для того чтобы компьютеры поддерживали актуальное время, они могут обращаться к серверам точного времени 
SNTP (Simple Network Time Protocol). К сожалению, компьютер не может просто получить время у сервера, 
потому что информация по сети передаётся не мгновенно: пока сообщение с текущим временем дойдёт до компьютера, 
оно потеряет свою актуальность. Протокол взаимодействия клиента (компьютера, запрашивающего точное время) 
и сервера (компьютера, выдающего точное время) выглядит следующим образом:

1. Клиент отправляет запрос на сервер и запоминает время отправления A (по клиентскому времени).

2. Сервер получает запрос в момент времени B (по точному серверному времени) и отправляет клиенту сообщение, 
содержащее время B.

3. Клиент получает ответ на свой запрос в момент времени C (по клиентскому времени) и запоминает его. Теперь 
клиент, из предположения, что сетевые задержки при передаче сообщений от клиента серверу и от сервера клиенту 
одинаковы, может определить и установить себе точное время, используя известные значения A, B, C.

Вам предстоит реализовать алгоритм, с точностью до секунды определяющий точное время для установки на клиенте 
по известным A, B и C. При необходимости округлите результат до целого числа секунд по правилам арифметики 
(в меньшую сторону, если дробная часть числа меньше 1/2, иначе в большую сторону).

Возможно, что, пока клиент ожидал ответа, по клиентскому времени успели наступить новые сутки, однако известно, 
что между отправкой клиентом запроса и получением ответа от сервера прошло менее 24 часов.

Формат ввода

Программа получает на вход три временные метки A, B, C, по одной в каждой строке. Все временные метки 
представлены в формате «hh:mm:ss», где «hh» – это часы, «mm» – минуты, «ss» – секунды. Часы, минуты 
и секунды записываются ровно двумя цифрами каждое (возможно, с дополнительными нулями в начале числа).

Формат вывода

Программа должна вывести одну временную метку в формате, описанном во входных данных, – вычисленное 
точное время для установки на клиенте. В выводе не должно быть пробелов, пустых строк в начале вывода.

Пример

Ввод
15:01:00
18:09:45
15:01:40

Вывод
18:10:05
*/

const fs = require('fs');

const input = fs.readFileSync('input.txt', 'utf8').toString();

const lines = input.trim().split('\n');

const clientSendTime = lines[0].trim().split(':');
const serverTime = lines[1].trim();
const clientReceiveTime = lines[2].trim().split(':');

const secs = parseInt(clientReceiveTime[2]) - parseInt(clientSendTime[2]);
const mins = parseInt(clientReceiveTime[1]) - parseInt(clientSendTime[1]);
let hrs = parseInt(clientReceiveTime[0]) - parseInt(clientSendTime[0]);

if (hrs < 0) {
  hrs += 24;
}

const time = Math.round((hrs * 3600 + mins * 60 + secs) / 2);

const serverDate = new Date('1970-01-01T' + serverTime);

const clientDate = new Date(serverDate.getTime() + time * 1000);

const output = 
  ('0' + clientDate.getHours()).slice(-2) + ':' +
  ('0' + clientDate.getMinutes()).slice(-2) + ':' +
  ('0' + clientDate.getSeconds()).slice(-2);

fs.writeFileSync('output.txt', output);
